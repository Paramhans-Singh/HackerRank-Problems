import java.util.Scanner;

public class Stdin_Stdout {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int b = s.nextInt();
        int n = s.nextInt();
        int a = s.nextInt();
        
        System.out.println(b);
        System.out.println(n);
        System.out.println(a);
        
s.close();
}
}
